            f_str = campo_funcion.Value;
            if isempty(f_str)
                return;
            end
            
            % Usar symbolic toolbox si est√° disponible
            syms x;
            f_sym = str2sym(f_str);
            df_sym = diff(f_sym, x);
            derivada_str = char(df_sym);
            
            campo_derivada.Value = derivada_str;
            
            % Actualizar resultados
            texto_resultados.Value = [{'=== DERIVADA CALCULADA AUTOM√ÅTICAMENTE ==='; ''}, ...
                                     {['f(x) = ' f_str]}; ...
                                     {['f''(x) = ' derivada_str]}; ...
                                     {''; 'Derivada calculada usando c√°lculo simb√≥lico.'; ...
                                      'Verifique que sea correcta antes de continuar.'}];
            
        catch
