                                     '', ...
                                     'POSIBLES CAUSAS Y SOLUCIONES:', ...
                                     '', ...
                                     'üîß Problemas comunes:', ...
                                     '‚Ä¢ Sintaxis incorrecta en funciones', ...
                                     '  ‚Üí Use sintaxis MATLAB: x^2, sin(x), exp(x), etc.', ...
                                     '', ...
                                     '‚Ä¢ Valor inicial inadecuado', ...
                                     '  ‚Üí Elija x‚ÇÄ m√°s cercano a la ra√≠z esperada', ...
                                     '  ‚Üí Evite puntos donde f''(x) = 0', ...
                                     '', ...
