            end
            
            texto_final = {['=== ' estado ' ==='], ...
                          '', ...
                          mensaje_conv, ...
                          '', ...
                          [icono_resultado ' RESULTADOS FINALES:'], ...
                          ['‚Ä¢ Funci√≥n: f(x) = ' f_str], ...
                          ['‚Ä¢ Derivada: f''(x) = ' df_str], ...
                          ['‚Ä¢ Valor inicial: x‚ÇÄ = ' num2str(x0)], ...
