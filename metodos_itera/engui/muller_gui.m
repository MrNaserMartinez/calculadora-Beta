    % FunciÃ³n principal de cÃ¡lculo
    function calcular_muller(~, ~)
        try
            % Limpiar resultados anteriores
            tabla_iteraciones.Data = {};
            cla(axes_grafica);
            
            % Obtener parÃ¡metros
            f_str = campo_funcion.Value;
            x0 = campo_x0.Value;
            x1 = campo_x1.Value;
            x2 = campo_x2.Value;
            tol = campo_tol.Value;
            max_iter = campo_max_iter.Value;
            
            % Validar puntos iniciales
            if x0 == x1 || x1 == x2 || x0 == x2
                error('Los tres puntos iniciales deben ser diferentes entre sÃ­.');
            end
            
            % Crear funciÃ³n anÃ³nima
            f = str2func(['@(x) ' f_str]);
            funcion_actual = f;
            
            % Verificar que la funciÃ³n se puede evaluar
            try
                test_vals = [f(x0), f(x1), f(x2)];
                if any(~isfinite(test_vals))
                    error('La funciÃ³n no se puede evaluar en uno o mÃ¡s puntos iniciales.');
                end
            catch
                error('Error al evaluar la funciÃ³n. Verifique la sintaxis.');
            end
            
            % InicializaciÃ³n
            iter = 0;
            error_actual = Inf;
            datos_tabla = {};
            
            texto_resultados.Value = {'=== EJECUTANDO MÃ‰TODO DE MÃœLLER ===', ...
                                     '', ...
                                     ['FunciÃ³n: f(x) = ' f_str], ...
                                     ['Puntos iniciales: xâ‚€ = ' num2str(x0) ', xâ‚ = ' num2str(x1) ', xâ‚‚ = ' num2str(x2)], ...
                                     ['Tolerancia: ' num2str(tol)], ...
                                     '', ...
                                     'Calculando iteraciones...'};
            drawnow;
            
            % Algoritmo de MÃ¼ller
            while error_actual > tol && iter < max_iter
                % Calcular diferencias divididas
                h1 = x1 - x0;
                h2 = x2 - x1;
                d1 = (f(x1) - f(x0)) / h1;
                d2 = (f(x2) - f(x1)) / h2;
                
                % Coeficientes de la parÃ¡bola
                a = (d2 - d1) / (h2 + h1);
                b = a * h2 + d2;
                c = f(x2);
                
                % Calcular discriminante
                discriminante = b^2 - 4 * a * c;
                rad = sqrt(discriminante);
                
                % Seleccionar denominador para minimizar error de cancelaciÃ³n
                if abs(b + rad) > abs(b - rad)
                    den = b + rad;
                else
                    den = b - rad;
                end
                
                if abs(den) < eps * 1000
                    error('DivisiÃ³n por cero detectada. El mÃ©todo no puede continuar.');
                end
                
                % Calcular nuevo punto
                dx = -2 * c / den;
                x3 = x2 + dx;
                error_actual = abs(dx);
                
                iter = iter + 1;
                
                % Guardar datos para la tabla
                datos_tabla{end+1, 1} = iter;
                datos_tabla{end, 2} = sprintf('%.6f', x0);
                datos_tabla{end, 3} = sprintf('%.6f', x1);
                datos_tabla{end, 4} = sprintf('%.6f', x2);
                datos_tabla{end, 5} = sprintf('%.8f', real(x3));
                datos_tabla{end, 6} = sprintf('%.2e', error_actual);
                
                try
                    fx3 = f(real(x3));
                    datos_tabla{end, 7} = sprintf('%.2e', abs(fx3));
                catch
                    datos_tabla{end, 7} = 'N/A';
                end
                
                % Actualizar puntos para siguiente iteraciÃ³n
                x0 = x1;
                x1 = x2;
                x2 = x3;
            end
            
            % Guardar raÃ­z encontrada
            raiz_encontrada = real(x2);
            
            % Actualizar tabla
            tabla_iteraciones.Data = datos_tabla;
            
            % Generar resultados finales
            if error_actual <= tol
                estado = 'âœ“ CONVERGENCIA EXITOSA';
                mensaje_convergencia = 'El mÃ©todo convergiÃ³ dentro de la tolerancia especificada.';
            else
                estado = 'âš ï¸ MÃXIMO DE ITERACIONES ALCANZADO';
                mensaje_convergencia = 'Se alcanzÃ³ el mÃ¡ximo de iteraciones sin convergencia completa.';
            end
            
            try
                valor_funcion = f(raiz_encontrada);
                texto_final = sprintf('f(%.8f) = %.2e', raiz_encontrada, valor_funcion);
            catch
                texto_final = 'Error al evaluar f(x) en la raÃ­z';
            end
            
            texto_resultados.Value = {['=== ' estado ' ==='], ...
                                     '', ...
                                     mensaje_convergencia, ...
                                     '', ...
                                     'ğŸ“Š RESULTADOS FINALES:', ...
                                     ['â€¢ RaÃ­z aproximada: x = ' sprintf('%.10f', raiz_encontrada)], ...
                                     ['â€¢ Valor de la funciÃ³n: ' texto_final], ...
                                     ['â€¢ Error final: ' sprintf('%.2e', error_actual)], ...
                                     ['â€¢ Iteraciones realizadas: ' num2str(iter)], ...
                                     '', ...
                                     'ğŸ“ˆ ANÃLISIS DE CONVERGENCIA:', ...
                                     ['â€¢ Tolerancia objetivo: ' sprintf('%.1e', tol)], ...
                                     ['â€¢ Velocidad: ' num2str(iter) ' iteraciones'], ...
                                     '', ...
                                     'La grÃ¡fica muestra la funciÃ³n y la raÃ­z encontrada.'};
            
            % Graficar automÃ¡ticamente
            graficar_funcion();
            
        catch e
            texto_resultados.Value = {'=== ERROR EN EL CÃLCULO ===', ...
                                     '', ...
                                     ['âŒ ' e.message], ...
                                     '', ...
                                     'POSIBLES CAUSAS Y SOLUCIONES:', ...
                                     '', ...
                                     'ğŸ”§ Errores comunes:', ...
                                     'â€¢ Sintaxis incorrecta en la funciÃ³n', ...
                                     '  â†’ Use .* ./ .^ para operaciones elemento a elemento', ...
                                     '  â†’ Ejemplo: x.^2 + sin(x) - 3', ...
                                     '', ...
                                     'â€¢ Puntos iniciales iguales', ...
                                     '  â†’ Los tres puntos xâ‚€, xâ‚, xâ‚‚ deben ser diferentes', ...
                                     '', ...
                                     'â€¢ FunciÃ³n no evaluable en puntos iniciales', ...
                                     '  â†’ Evite divisiones por cero o logaritmos de nÃºmeros negativos', ...
                                     '', ...
                                     'â€¢ DivisiÃ³n por cero durante iteraciones', ...
                                     '  â†’ Intente con puntos iniciales diferentes'};
        end
    end
    
    % FunciÃ³n para graficar
    function graficar_funcion(~, ~)
        if isempty(funcion_actual)
            return;
        end
        
        try
            % Determinar rango de graficaciÃ³n
            if ~isempty(raiz_encontrada)
                x_center = raiz_encontrada;
            else
                x_center = (campo_x0.Value + campo_x1.Value + campo_x2.Value) / 3;
            end
            
            x_range = [x_center - 5, x_center + 5];
            x_vals = linspace(x_range(1), x_range(2), 500);
            
            % Evaluar funciÃ³n
            try
                y_vals = arrayfun(funcion_actual, x_vals);
            catch
                % Si hay problemas con arrayfun, usar un loop
                y_vals = zeros(size(x_vals));
                for i = 1:length(x_vals)
                    try
                        y_vals(i) = funcion_actual(x_vals(i));
                    catch
                        y_vals(i) = NaN;
                    end
                end
            end
            
            % Graficar funciÃ³n
            plot(axes_grafica, x_vals, y_vals, 'b-', 'LineWidth', 2);
            hold(axes_grafica, 'on');
            
            % LÃ­nea en y = 0
            plot(axes_grafica, x_range, [0, 0], 'k--', 'LineWidth', 1);
            
            % Marcar puntos iniciales
            x0_val = campo_x0.Value;
            x1_val = campo_x1.Value;
            x2_val = campo_x2.Value;
            
            try
                plot(axes_grafica, x0_val, funcion_actual(x0_val), 'go', 'MarkerSize', 8, 'LineWidth', 2);
                plot(axes_grafica, x1_val, funcion_actual(x1_val), 'go', 'MarkerSize', 8, 'LineWidth', 2);
                plot(axes_grafica, x2_val, funcion_actual(x2_val), 'go', 'MarkerSize', 8, 'LineWidth', 2);
            catch
                % Continuar si no se pueden evaluar los puntos iniciales
            end
            
            % Marcar raÃ­z encontrada
            if ~isempty(raiz_encontrada)
                try
                    y_raiz = funcion_actual(raiz_encontrada);
                    plot(axes_grafica, raiz_encontrada, y_raiz, 'ro', 'MarkerSize', 12, 'LineWidth', 3);
                    
                    % Etiqueta de la raÃ­z
                    text(axes_grafica, raiz_encontrada, y_raiz + (max(y_vals) - min(y_vals)) * 0.1, ...
                         sprintf('RaÃ­z: %.4f', raiz_encontrada), ...
                         'FontSize', 10, 'FontWeight', 'bold', ...
                         'HorizontalAlignment', 'center', ...
                         'BackgroundColor', 'white', ...
                         'EdgeColor', 'red');
                catch
                    % Continuar si no se puede evaluar en la raÃ­z
                end
            end
            
            grid(axes_grafica, 'on');
            title(axes_grafica, 'MÃ©todo de MÃ¼ller - FunciÃ³n y RaÃ­z Aproximada', 'FontSize', 12);
            xlabel(axes_grafica, 'x');
            ylabel(axes_grafica, 'f(x)');
            
            if ~isempty(raiz_encontrada)
                legend(axes_grafica, {'f(x)', 'y = 0', 'Puntos iniciales', '', '', 'RaÃ­z encontrada'}, ...
                       'Location', 'best');
            else
                legend(axes_grafica, {'f(x)', 'y = 0', 'Puntos iniciales'}, ...
                       'Location', 'best');
            end
            
            hold(axes_grafica, 'off');
            
        catch
            text(axes_grafica, 0.5, 0.5, 'Error al generar grÃ¡fica', ...
                 'Units', 'normalized', ...
                 'HorizontalAlignment', 'center', ...
                 'FontSize', 12, 'Color', 'red');
        end
    end
    
