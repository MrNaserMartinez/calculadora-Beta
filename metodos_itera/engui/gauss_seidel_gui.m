    % Funci√≥n para verificar matriz
    function verificar_matriz(~, ~)
        try
            A_str = campo_matriz_a.Value{1};
            A = eval(A_str);
            
            [n, m] = size(A);
            if n ~= m
                texto_verificacion.Text = '‚ùå No es cuadrada';
                texto_verificacion.FontColor = [0.8, 0.2, 0.2];
                return;
            end
            
            % Verificar elementos diagonales no nulos
            if any(diag(A) == 0)
                texto_verificacion.Text = '‚ùå Diag. con ceros';
                texto_verificacion.FontColor = [0.8, 0.2, 0.2];
                return;
            end
            
            % Verificar dominancia diagonal
            dominante = true;
            for i = 1:n
                suma_fila = sum(abs(A(i, :))) - abs(A(i, i));
                if abs(A(i, i)) <= suma_fila
                    dominante = false;
                    break;
                end
            end
            
            if dominante
                texto_verificacion.Text = '‚úì Matriz v√°lida';
                texto_verificacion.FontColor = [0.2, 0.7, 0.2];
            else
                texto_verificacion.Text = '‚ö†Ô∏è No dom. diagonal';
                texto_verificacion.FontColor = [0.8, 0.6, 0.2];
            end
            
        catch
            texto_verificacion.Text = '‚ùå Error sintaxis';
            texto_verificacion.FontColor = [0.8, 0.2, 0.2];
        end
    end
    
