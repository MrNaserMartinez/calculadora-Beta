    % Funci√≥n principal para resolver el sistema
    function resolver_sistema(~, ~)
        try
            % Limpiar resultados anteriores
            tabla_iteraciones.Data = {};
            cla(axes_grafica);
            
            % Obtener datos de entrada
            A_str = campo_matriz_a.Value{1};
            b_str = campo_vector_b.Value;
            x0_str = campo_vector_x0.Value;
            tol = campo_tol.Value;
            max_iter = campo_max_iter.Value;
            
            % Evaluar matrices y vectores
            A = eval(A_str);
            b = eval(b_str);
            x0 = eval(x0_str);
            
            % Almacenar para otras funciones
            matriz_A = A;
            vector_b = b;
            vector_x0 = x0;
            
            % Verificaciones b√°sicas
            [n, m] = size(A);
            if n ~= m
                error('La matriz A debe ser cuadrada.');
            end
            
            b = b(:); % Vector columna
            x0 = x0(:); % Vector columna
            
            if length(b) ~= n || length(x0) ~= n
                error('Los vectores b y x‚ÇÄ deben tener la misma dimensi√≥n que A.');
            end
            
            % Verificar elementos diagonales
            if any(diag(A) == 0)
                error('La matriz tiene elementos diagonales nulos.');
            end
            
            % Inicializaci√≥n del algoritmo
            x = x0;
            error_actual = Inf;
            iter = 0;
            datos_tabla = {};
            errores = [];
            
            texto_resultados.Value = {'=== EJECUTANDO M√âTODO DE GAUSS-SEIDEL ===', ...
                                     '', ...
                                     ['Dimensi√≥n del sistema: ' num2str(n) '√ó' num2str(n)], ...
                                     ['Tolerancia: ' sprintf('%.1e', tol)], ...
                                     ['M√°ximo de iteraciones: ' num2str(max_iter)], ...
                                     '', ...
                                     'Calculando iteraciones...'};
            drawnow;
            
            % Algoritmo de Gauss-Seidel
            while error_actual > tol && iter < max_iter
                x_old = x;
                
                % Actualizar cada componente
                for i = 1:n
                    suma1 = 0;
                    suma2 = 0;
                    
                    % Suma de elementos antes de la diagonal (usa x actualizado)
                    if i > 1
                        suma1 = A(i, 1:i-1) * x(1:i-1);
                    end
                    
                    % Suma de elementos despu√©s de la diagonal (usa x anterior)
                    if i < n
                        suma2 = A(i, i+1:end) * x_old(i+1:end);
                    end
                    
                    x(i) = (b(i) - suma1 - suma2) / A(i, i);
                end
                
                iter = iter + 1;
                error_actual = norm(x - x_old, Inf);
                errores(end+1) = error_actual;
                
                % Guardar datos para la tabla (limitar a 6 variables mostradas)
                fila_datos = {iter};
                for j = 1:min(n, 5)
                    fila_datos{end+1} = sprintf('%.6f', x(j));
                end
                % Rellenar columnas faltantes si n < 5
                while length(fila_datos) < 6
                    fila_datos{end+1} = '';
                end
                fila_datos{end+1} = sprintf('%.2e', error_actual);
                
                datos_tabla{end+1, 1} = fila_datos{1};
                for k = 2:length(fila_datos)
                    datos_tabla{end, k} = fila_datos{k};
                end
            end
            
            % Guardar resultados
            solucion_final = x;
            datos_iteraciones = datos_tabla;
            errores_convergencia = errores;
            
            % Actualizar tabla
            tabla_iteraciones.Data = datos_tabla;
            
            % Generar resultados finales
            if error_actual <= tol
                estado = '‚úì CONVERGENCIA EXITOSA';
                mensaje_convergencia = 'El m√©todo convergi√≥ dentro de la tolerancia especificada.';
            else
                estado = '‚ö†Ô∏è M√ÅXIMO DE ITERACIONES ALCANZADO';
                mensaje_convergencia = 'Se alcanz√≥ el m√°ximo de iteraciones sin convergencia completa.';
            end
            
            % Verificar soluci√≥n
            residuo = A * x - b;
            norma_residuo = norm(residuo);
            
            texto_final = {['=== ' estado ' ==='], ...
                          '', ...
                          mensaje_convergencia, ...
                          '', ...
                          'üìä RESULTADOS FINALES:', ...
                          ['‚Ä¢ Iteraciones realizadas: ' num2str(iter)], ...
                          ['‚Ä¢ Error final: ' sprintf('%.2e', error_actual)], ...
                          ['‚Ä¢ Norma del residuo: ' sprintf('%.2e', norma_residuo)], ...
                          '', ...
                          'üéØ SOLUCI√ìN ENCONTRADA:'};
            
            for i = 1:n
                texto_final{end+1} = ['‚Ä¢ x‚Çç' num2str(i) '‚Çé = ' sprintf('%.8f', x(i))];
            end
            
            texto_final{end+1} = '';
            texto_final{end+1} = 'üîç VERIFICACI√ìN:';
            texto_final{end+1} = 'Ax - b = [';
            for i = 1:n
                if i < n
                    texto_final{end} = [texto_final{end} sprintf('%.2e; ', residuo(i))];
                else
                    texto_final{end} = [texto_final{end} sprintf('%.2e]', residuo(i))];
                end
            end
            
            if norma_residuo < tol * 10
                texto_final{end+1} = '‚úì Soluci√≥n verificada correctamente';
            else
                texto_final{end+1} = '‚ö†Ô∏è Residuo alto - revisar condicionamiento';
            end
            
            texto_resultados.Value = texto_final;
            
            % Graficar convergencia autom√°ticamente
            graficar_convergencia();
            
        catch e
            texto_resultados.Value = {'=== ERROR EN EL C√ÅLCULO ===', ...
                                     '', ...
                                     ['‚ùå ' e.message], ...
                                     '', ...
                                     'POSIBLES CAUSAS Y SOLUCIONES:', ...
                                     '', ...
                                     'üîß Errores comunes:', ...
                                     '‚Ä¢ Sintaxis incorrecta en matrices/vectores', ...
                                     '  ‚Üí Use formato MATLAB: [a b; c d] para matrices', ...
                                     '  ‚Üí Use [a; b] o [a b] para vectores', ...
                                     '', ...
                                     '‚Ä¢ Dimensiones incompatibles', ...
                                     '  ‚Üí Matriz A debe ser n√ón', ...
                                     '  ‚Üí Vectores b y x‚ÇÄ deben ser de tama√±o n', ...
                                     '', ...
                                     '‚Ä¢ Matriz no apropiada', ...
                                     '  ‚Üí Elementos diagonales no pueden ser cero', ...
                                     '  ‚Üí Se recomienda dominancia diagonal', ...
                                     '', ...
                                     '‚Ä¢ Par√°metros inv√°lidos', ...
                                     '  ‚Üí Tolerancia debe ser positiva', ...
                                     '  ‚Üí M√°ximo de iteraciones debe ser entero positivo'};
        end
    end
    
